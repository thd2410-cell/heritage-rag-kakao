def main():
    path = BASE / "testset.json"
    ts = json.load(open(path, encoding="utf-8"))
    n = 0
    for q in ts:
        q["expected_heritage"] = EXPECTED.get(q["id"], [])  # 없으면 빈 리스트
        if q["expected_heritage"]:
            n += 1
    json.dump(ts, open(path, "w", encoding="utf-8"), ensure_ascii=False, indent=2)
    print(f"expected_heritage 추가: {n}문항에 정답 유산 라벨, 총 {len(ts)}문항")


if __name__ == "__main__":
    main()
